num1 = float(input("Please Enter a number : "))
op = input("Please enter operation : ")
num2 = float(input("Please enter second number"))

if op == "+":
    print(num1 + num2)
elif op == "-":
    print(num1 - num2)
elif op == "/":
    print(num1 / num2)
elif op == "*":
    print(num1 * num2)
else:
    print("Invalid imp")


